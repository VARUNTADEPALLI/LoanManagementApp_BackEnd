package com.varun.dto;

public class RemainingPaymentDto {

	double remainingPrincipal;
	double remainingIntrest;
	double intrestPaid;
	double principalPaid;
	double remainingInstallment;
	double installmentPaid;
	
	public double getRemainingPrincipal() {
		return remainingPrincipal;
	}
	public void setRemainingPrincipal(double remainingPrincipal) {
		this.remainingPrincipal = remainingPrincipal;
	}
	public double getRemainingIntrest() {
		return remainingIntrest;
	}
	public void setRemainingIntrest(double remainingIntrest) {
		this.remainingIntrest = remainingIntrest;
	}
	public double getIntrestPaid() {
		return intrestPaid;
	}
	public void setIntrestPaid(double intrestPaid) {
		this.intrestPaid = intrestPaid;
	}
	public double getPrincipalPaid() {
		return principalPaid;
	}
	public void setPrincipalPaid(double principalPaid) {
		this.principalPaid = principalPaid;
	}
	public double getRemainingInstallment() {
		return remainingInstallment;
	}
	public void setRemainingInstallment(double remainingInstallment) {
		this.remainingInstallment = remainingInstallment;
	}
	public double getInstallmentPaid() {
		return installmentPaid;
	}
	public void setInstallmentPaid(double installmentPaid) {
		this.installmentPaid = installmentPaid;
	}
	
	
}
